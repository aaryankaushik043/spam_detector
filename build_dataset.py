def build():
    """Write the curated dataset to CSV."""
    os.makedirs(DATA_DIR, exist_ok=True)

    rows = []
    for text in SPAM:
        rows.append({"label": "spam", "text": text})
    for text in HAM:
        rows.append({"label": "ham", "text": text})

    with open(DATA_FILE, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["label", "text"])
        writer.writeheader()
        writer.writerows(rows)

    print(f"Dataset written to {DATA_FILE}")
    print(f"  Spam messages : {len(SPAM)}")
    print(f"  Ham  messages : {len(HAM)}")
    print(f"  Total         : {len(SPAM) + len(HAM)}")


if __name__ == "__main__":
    build()
